#as previously we declare intended numbers in form of num
num1= 8
num2= 2
#substraction of num
sub = num1 - num2
print('Substraction of {0} and {1} is {2}'.format(num1, num2, sub))

#the same applies to multiple numbers
num1= 8
num2= 2
num3= 3 
num4= 1
sub = num1 - num2 - num3 - num4 

print('Substraction of {0} - {1} - {2} - {3} is {4}'.format(num1, num2, num3, num4, sub))